class RomanConverter {
    /**
     * Roman to decimal converter
     *
     * @param n number in decimal format
     * @return string representation of the number in Roman numeral
     */
    public static String toRoman(int n) {


        String[] thousands = {"", "M", "MM", "MMM", "MMMM", "MMMMM", "MMMMMM", "MMMMMMM", "MMMMMMMM", "MMMMMMMMM"};
        String[] hundreds = {"", "C", "CC", "CCC", "CD", "D", "DC", "DCC", "DCCC", "CM"};
        String[] tens = {"", "X", "XX", "XXX", "XL", "L", "LX", "LXX", "LXXX", "XC"};
        String[] ones = {"", "I", "II", "III", "IV", "V", "VI", "VII", "VIII", "IX"};

        StringBuilder roman = new StringBuilder();
        ///123.4
        roman.append(thousands[n / 1000]);
        roman.append(hundreds[(n/100) % 10]);
        roman.append(tens[(n/10) % 10]);
        roman.append(ones[(n % 10)]);

//        for (int i = 4000; i <= n; i += 1000) {
//            roman.insert(0, "M");
//        }

        return roman.toString();
    }

}
